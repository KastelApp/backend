const { model, Schema } = require('mongoose');
const { PRESENCE } = require('../../../constants');

const settingSchema = new Schema({
    user: {
        type: String,
        ref: 'users',
        required: true,
    },

    status: {
        type: String,
        required: false,
    },

    presence: {
        type: Number,
        required: true,
        default: PRESENCE.OFFLINE,
    },
});

module.exports = model('settings', settingSchema);